# Lesson 1: Python Basics and Hello World Program

# print()函数用来显示文本 - print() function displays text
print("你好，世界！")  # Hello, World! in Chinese
print("Hello, World!")

# 显示不同的消息 - Display different messages
print("欢迎来到Python世界")  # Welcome to Python world
print("Welcome to Python world")

# 你可以打印数字 - You can print numbers
print(2025)
print(42)

# 数学运算 - Math operations
print(10 + 5)   # 加法 Addition
print(10 - 3)   # 减法 Subtraction
print(4 * 5)    # 乘法 Multiplication
print(15 / 3)   # 除法 Division

# 多行输出 - Multiple line output
print("第一行")  # First line
print("第二行")  # Second line
print("第三行")  # Third line
